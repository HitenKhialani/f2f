export const normalizeVoiceInput = (transcript) => {
  if (!transcript) return '';
  const lower = transcript.toLowerCase().trim();
  
  // Try direct match first
  if (voiceSynonyms[lower]) {
    return voiceSynonyms[lower];
  }
  
  // Try to find if the transcript contains any of our synonyms
  for (const [synonym, normalized] of Object.entries(voiceSynonyms)) {
    if (lower.includes(synonym)) {
      return normalized;
    }
  }
  
  // Handle numbers separately (e.g. for quantity or price)
  const numbersMatch = lower.match(/\d+/);
  if (numbersMatch) {
    return numbersMatch[0]; // Return the first number found
  }
  
  return lower; // Return original if no match
};
